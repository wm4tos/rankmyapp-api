const { Schema, model } = require('mongoose');

const userSchema = new Schema({
  id: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports.userSchema = userSchema;

module.exports = model('users', userSchema);
